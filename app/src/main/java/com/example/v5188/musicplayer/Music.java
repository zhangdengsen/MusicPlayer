package com.example.v5188.musicplayer;

public class Music {
    String tupian;//图片
    String geming;//歌名
    String geshou;//歌手
    String dizhi;//地址

    public String getDizhi() {
        return dizhi;
    }

    public void setDizhi(String dizhi) {
        this.dizhi = dizhi;
    }

    public String getGeming() {
        return geming;
    }

    public void setGeming(String geming) {
        this.geming = geming;
    }

    public String getGeshou() {
        return geshou;
    }

    public void setGeshou(String geshou) {
        this.geshou = geshou;
    }

    public String getTupian() {
        return tupian;
    }

    public void setTupian(String tupian) {
        this.tupian = tupian;
    }

}
